    private void jButtonAjouterLivreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAjouterLivreActionPerformed
        // TODO add your handling code here:
   
        //Creer la fenetre pour creer le livre dans le PaneControle
        app.controleur.setjInternal(new JInternalFrameCreerLivre());
        this.jDesktopPaneControle.add(app.controleur.getjInternal());
        
        
       
        app.controleur.getjInternal().setControleur(app.controleur);
        app.controleur.getjInternal().setjFrame(this);
        
        
        // Ajouter les infos des tables format, edition, auteur et genre dans les menus déroulants
        try {
            ArrayList<TableAnnexe> tableFormat = app.tableLivres.lireTableAnnexe("format");
            ArrayList<TableAnnexe> tableEdition = app.tableLivres.lireTableAnnexe("edition");
            ArrayList<TableAnnexe> tableAuteur = app.tableLivres.lireTableAnnexe("auteur");
            ArrayList<TableAnnexe> tableGenre = app.tableLivres.lireTableAnnexe("genre");
            app.listIdAuteur = app.controleur.getjInternal().controleur.setComboBox(app.controleur.getjInternal().getjComboBoxAuteur(), tableAuteur);
            app.listIdEdition = app.controleur.getjInternal().controleur.setComboBox(app.controleur.getjInternal().getjComboBoxEdition(), tableEdition);
            app.listIdGenre = app.controleur.getjInternal().controleur.setComboBox(app.controleur.getjInternal().getjComboBoxGenre(), tableGenre);
            app.listIdFormat = app.controleur.getjInternal().controleur.setComboBox(app.controleur.getjInternal().getjComboBoxFormat(), tableFormat);

        } catch (SQLException ex) {
            Logger.getLogger(JFrameBibliotheque.class.getName()).log(Level.SEVERE, null, ex);
        }

        app.controleur.getjInternal().show();


    }//GEN-LAST:event_jButtonAjouterLivreActionPerformed

