    private void jToggleButtonSupprimerLivreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButtonSupprimerLivreActionPerformed
      
        int selectedRow = this.jTableBibliotheque.getSelectedRow();
        String idLivre = String.valueOf(this.jTableBibliotheque.getModel().getValueAt(selectedRow, 9));
        app.tableLivres.supprimerLivre(idLivre);
        app.majBiblio(app.tableLivres.lireLivresParID(app.admi.getIdUtilisateur()),app.admi);
    }//GEN-LAST:event_jToggleButtonSupprimerLivreActionPerformed

